# Escape de cadenas
# \'      Comilla simple
# \\      Barra invertida
# \n      Nueva línea
# \r      Retorno
# \t      Tabulador
# \b      Retroceso
# \f      Alimentación de formularios
# \ooo    Valor octal
# \xhh    Valor hexadecimal
a = 'Hello, \\ World!'
print(a)
a = 'Hello, \n World!'
print(a)
a = 'Hello, \t World!'
print(a)

