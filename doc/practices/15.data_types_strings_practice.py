# Prácticas de cadenas

# 1. Utilice el método len para imprimir la longitud de la cadena.
x = "Erick Agrazal"
print(len(x))

# 2. Obtener el primer carácter de la cadena txt.
txt = "Hello, World!"
print(txt[0])

# 3. Obtenga los caracteres del índice 2 al índice 4 (llo).
txt = "Hello World"
print(txt[2:5])

# 4. Devuelva la cadena sin ningún espacio en blanco al principio o al final.
txt = " Hello World "
print(txt.strip())

# 5. Convierte el valor de txt a mayúsculas.
txt = "Hello, World!"
print(txt.upper())

# 6. Convertir el valor de txt a minúsculas.
txt = "Hello, World!"
print(txt.lower())

# 7. Sustituir el carácter H por una J.
txt = "Hello, World!"
print(txt.replace("H", "J"))
