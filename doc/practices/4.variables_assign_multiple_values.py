# Asignación múltiple de variables
x, y, z = "Naranja", "Uva", "Pera"
print(x)
print(y)
print(z)

# Asignación de un valor a multiples variables
x = y = z = 123
print(x)
print(y)
print(z)

# Desempaquetar una colección
frutas = ["manzana", "banana", "piña"]
x, y, z = frutas
print(x)
print(y)
print(z)
