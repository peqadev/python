# Listas
a = ['manzana', 'banana', 'arándano']
print(a)
print(a[0])  # manzana
print(a[1])  # banana

# Permite duplicados
a = ['manzana', 'banana', 'arándano', 'arándano']
print(a)

# Obtener tamaño de una lista
a = ['manzana', 'banana', 'arándano', 'arándano', ]
print(len(a))

# Tipos de datos de los elementos de una lista
a = [1, 2, 3, 4, ]
print(a)
a = [True, False, ]
print(a)
a = [1, True, 'manaza', ['banana', 'arándano'], 12.12, ]
print(a)

# Leer tipo de lista
a = [1, 2, 3]
print(type(a))

# Cast a una lista
a = list((1, 2, 3))
print(a)
